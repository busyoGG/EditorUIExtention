namespace EditorUIExtension
{
    public struct IntVec4
    {
        public IntVec4(int x,int y,int z,int w)
        {
            this.x = x;
            this.y = y;
            this.z = z;
            this.w = w;
        }
        
        public int x;
        public int y;
        public int z;
        public int w;
    }
}